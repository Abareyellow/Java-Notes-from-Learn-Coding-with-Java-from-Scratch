public class datatypes {
  public static void main(String[] args) {
    // Data Types in Java
    int newTemp = 30;
    float newFloat = 10.75f;
    boolean isCorrect = true;
    char newLetter = 'E';
    String newMessage = "Java is powerful";
    // Other types
    byte newVal = 120;
    short newVal2 = 30000;
    long newLongVal = 30000000000000000L;
    double newDescimal = 39.87d;

    System.out.println(newTemp);
    System.out.println(newFloat);
    System.out.println(isCorrect);
    System.out.println(newLetter);
    System.out.println(newMessage);
    System.out.println(newVal);
    System.out.println(newVal2);
    System.out.println(newLongVal);
    System.out.println(newDescimal);
  }
}